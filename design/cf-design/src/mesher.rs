//! Marching cubes mesher with interval arithmetic pruning.
//!
//! Extracts a triangle mesh from an implicit surface field at the zero
//! isosurface. Grid cells that are entirely inside or entirely outside
//! (determined via [`FieldNode::evaluate_interval`]) are skipped, typically
//! pruning 80%+ of cells for simple primitives.

use std::collections::HashMap;

use cf_geometry::{Aabb, IndexedMesh};
use mesh_types::AttributedMesh;
use nalgebra::Point3;

use crate::field_node::FieldNode;

/// Statistics from the meshing process.
#[allow(dead_code)]
pub struct MeshStats {
    /// Total number of grid cells considered.
    pub cells_total: usize,
    /// Number of cells skipped by interval pruning.
    pub cells_pruned: usize,
}

/// Extract an isosurface mesh from a field node using marching cubes.
///
/// `cell_size` controls the voxel resolution (smaller = finer mesh).
/// Interval arithmetic prunes cells that are entirely inside or outside
/// the surface, skipping most of the grid.
///
/// Vertices on shared cell edges are deduplicated via an edge cache,
/// producing a properly indexed manifold mesh. The returned
/// [`AttributedMesh`] carries analytical surface normals (`normals = Some`)
/// computed from the SDF gradient at each emitted vertex.
// Index/count conversion bounded by domain.
#[allow(
    clippy::cast_possible_truncation,
    clippy::cast_sign_loss,
    clippy::cast_precision_loss,
    clippy::too_many_lines
)]
pub fn mesh_field(node: &FieldNode, bounds: &Aabb, cell_size: f64) -> (AttributedMesh, MeshStats) {
    let size = bounds.size();
    let nx = ((size.x / cell_size).ceil() as usize).max(1);
    let ny = ((size.y / cell_size).ceil() as usize).max(1);
    let nz = ((size.z / cell_size).ceil() as usize).max(1);

    let origin = bounds.min;

    let mut mesh = IndexedMesh::new();
    let mut normals: Vec<nalgebra::Vector3<f64>> = Vec::new();
    let mut corner_cache: HashMap<(usize, usize, usize), f64> = HashMap::new();
    let mut edge_cache: HashMap<(usize, usize, usize, u8), u32> = HashMap::new();

    let mut cells_total = 0_usize;
    let mut cells_pruned = 0_usize;

    for cz in 0..nz {
        for cy in 0..ny {
            for cx in 0..nx {
                cells_total += 1;

                // Cell AABB for interval evaluation
                let cell_min = Point3::new(
                    (cx as f64).mul_add(cell_size, origin.x),
                    (cy as f64).mul_add(cell_size, origin.y),
                    (cz as f64).mul_add(cell_size, origin.z),
                );
                let cell_max = Point3::new(
                    ((cx + 1) as f64).mul_add(cell_size, origin.x),
                    ((cy + 1) as f64).mul_add(cell_size, origin.y),
                    ((cz + 1) as f64).mul_add(cell_size, origin.z),
                );
                let cell_aabb = Aabb::new(cell_min, cell_max);

                // Interval pruning: skip cells entirely inside or outside
                let (lo, hi) = node.evaluate_interval(&cell_aabb);
                if lo > 0.0 || hi < 0.0 {
                    cells_pruned += 1;
                    continue;
                }

                // Get 8 corner values and positions
                let mut values = [0.0_f64; 8];
                let mut corners = [Point3::origin(); 8];
                for (i, &(dx, dy, dz)) in CORNER_OFFSETS.iter().enumerate() {
                    let gx = cx + dx;
                    let gy = cy + dy;
                    let gz = cz + dz;
                    let p = Point3::new(
                        (gx as f64).mul_add(cell_size, origin.x),
                        (gy as f64).mul_add(cell_size, origin.y),
                        (gz as f64).mul_add(cell_size, origin.z),
                    );
                    corners[i] = p;
                    values[i] = if let Some(&v) = corner_cache.get(&(gx, gy, gz)) {
                        v
                    } else {
                        let v = node.evaluate(&p);
                        corner_cache.insert((gx, gy, gz), v);
                        v
                    };
                }

                // Compute cube index: bit set for corners with value < 0 (inside)
                let mut cube_index = 0_u8;
                for (i, &val) in values.iter().enumerate() {
                    if val < 0.0 {
                        cube_index |= 1 << i;
                    }
                }

                // Skip if all corners are inside or all are outside
                if cube_index == 0 || cube_index == 255 {
                    continue;
                }

                let edge_mask = EDGE_TABLE[cube_index as usize];
                if edge_mask == 0 {
                    continue;
                }

                // Compute or retrieve edge vertex indices
                let mut edge_verts = [0_u32; 12];
                for edge_idx in 0..12_usize {
                    if edge_mask & (1 << edge_idx) != 0 {
                        let (kx, ky, kz, axis) = EDGE_KEY_OFFSETS[edge_idx];
                        let key = (cx + kx, cy + ky, cz + kz, axis);

                        edge_verts[edge_idx] = if let Some(&idx) = edge_cache.get(&key) {
                            idx
                        } else {
                            let (c0, c1) = EDGE_CORNERS[edge_idx];
                            let p = interpolate_vertex(
                                corners[c0],
                                corners[c1],
                                values[c0],
                                values[c1],
                            );
                            let idx = mesh.vertices.len() as u32;
                            mesh.vertices.push(p);
                            normals.push(node.gradient(&p));
                            edge_cache.insert(key, idx);
                            idx
                        };
                    }
                }

                // Generate triangles (swap e1/e2 for CCW outward-facing winding)
                let tri = &TRI_TABLE[cube_index as usize];
                let mut t = 0;
                while t < 16 && tri[t] != -1 {
                    let e0 = tri[t] as usize;
                    let e1 = tri[t + 1] as usize;
                    let e2 = tri[t + 2] as usize;
                    mesh.faces
                        .push([edge_verts[e0], edge_verts[e2], edge_verts[e1]]);
                    t += 3;
                }
            }
        }
    }

    let mut attributed = AttributedMesh::new(mesh);
    attributed.normals = Some(normals);

    (
        attributed,
        MeshStats {
            cells_total,
            cells_pruned,
        },
    )
}

/// Interpolate vertex position along an edge at the zero crossing.
fn interpolate_vertex(p0: Point3<f64>, p1: Point3<f64>, v0: f64, v1: f64) -> Point3<f64> {
    let denom = v1 - v0;
    if denom.abs() < f64::EPSILON {
        return Point3::from((p0.coords + p1.coords) * 0.5);
    }
    let t = (-v0 / denom).clamp(0.0, 1.0);
    Point3::from(p0.coords * (1.0 - t) + p1.coords * t)
}

