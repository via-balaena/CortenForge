/// Corner offsets within a cell: maps corner index to (dx, dy, dz) offset
/// from the cell's minimum grid coordinate.
const CORNER_OFFSETS: [(usize, usize, usize); 8] = [
    (0, 0, 0), // corner 0
    (1, 0, 0), // corner 1
    (1, 1, 0), // corner 2
    (0, 1, 0), // corner 3
    (0, 0, 1), // corner 4
    (1, 0, 1), // corner 5
    (1, 1, 1), // corner 6
    (0, 1, 1), // corner 7
];

/// Edge-to-corner mapping: each MC edge connects two corners.
const EDGE_CORNERS: [(usize, usize); 12] = [
    (0, 1),
    (1, 2),
    (2, 3),
    (3, 0),
    (4, 5),
    (5, 6),
    (6, 7),
    (7, 4),
    (0, 4),
    (1, 5),
    (2, 6),
    (3, 7),
];

/// Edge cache key offsets: maps MC edge index to (dx, dy, dz, axis) offset
/// from the cell coordinate. The canonical key for a grid edge is the lower
