//! F3 procedure-spec markdown generator.
//!
//! [`generate_procedure_markdown`] renders a workshop-ready Markdown
//! procedure for a [`CastSpec`]: per-layer pour masses (from F2),
//! mix ratio + pot life + cure time (from [`crate::cure::lookup`]),
//! generic Smooth-On guidance, and a mass-budget summary. The
//! generated markdown is the starting-point reference the workshop
//! user iterates against bench reality after iter-1.
//!
//! The function is pure (no IO) so tests can assert on its output
//! bit-exactly. [`CastSpec::write_procedure`] is the convenience
//! wrapper that writes the rendered string to disk.

// `writeln!`/`write!` on a `String` go through the `core::fmt::Write`
// trait, which returns `Result<(), fmt::Error>` for generality. The
// `String` impl never fails, so the workspace's
// `let_underscore_must_use` deny is structurally redundant on every
// `let _ = writeln!(md, …);` site in this generator. The
// alternatives (an `.expect("…")` chain, a `push_str(&format!(…))`
// rewrite at every site) trade one form of noise for another;
// scoping the allow to this single template module keeps the
// workspace-wide deny intact for code where Result-discarding
// genuinely could mask failures.
#![allow(clippy::let_underscore_must_use)]

use std::fmt::Write as _;

use crate::cure::lookup as lookup_cure;
